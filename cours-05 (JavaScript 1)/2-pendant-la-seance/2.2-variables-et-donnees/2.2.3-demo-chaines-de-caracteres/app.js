// Chaînes de caractères
// Définition avec guillemets doubles ou simples
console.log("Bonsoir à toutes et tous");
console.log('Bonsoir à toutes et tous');

// On utilise les guillemets de manière astucieuse
console.log("J'ai faim");

// Échappement
console.log('Elle a dit: "J\'ai faim"');

var prenom = "Camille";
console.log(prenom);

// Concaténation (addition de texte)
console.log("Bonjour, " + prenom);

// On peut aussi faire la concaténation d'un (ou plusieurs) nombre
// avec une (ou plusieurs) chaîne de caractères (TP2).
var montant = 50;
var devise = "yen";
console.log("Le total est: " + montant + devise);

